 class Shift {
  public static void main (String args[]) {
    int x = 7;
    System.out.println("x = " + x);               // 7
    System.out.println("x >> 2 = " + (x >> 2));   // 1
    System.out.println("x << 1 = " + (x << 1));   // 13
    System.out.println("x >>> 1 = " + (x >>> 1)); // 3
  }
}