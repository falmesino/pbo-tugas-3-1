 class Bitwise {
  public static void main (String args[]) {
    int x = 5;
    int y = 6;

    System.out.println("x = " + x);           // 5
    System.out.println("y = " + y);           // 6
    System.out.println("x & y = " + (x & y)); // 4
    System.out.println("x | y = " + (x | y)); // 7
    System.out.println("x ^ y = " + (x ^ y)); // 3
  }
}